// 辅助：将 {task, days} 展开为多天条目
// Day1=看视频+理解概念, Day2=做例题+习题, Day3(若有)=难题突破+总结
function makeMultiDay(entries) {
  const result = [];
  for (const { task, days } of entries) {
    if (days === 1) {
      result.push(task + ' (看视频+全部例题)');
    } else if (days === 2) {
      result.push(task + ' D1: 看视频+理解概念+笔记');
      result.push(task + ' D2: 全部例题+配套习题+整理错题');
    } else {
      result.push(task + ' D1: 看视频+理解核心概念+笔记');
      result.push(task + ' D2: 精讲例题+专项练习');
      result.push(task + ' D3: 综合难题+错题整理+向后总结');
    }
  }
  return result;
}

