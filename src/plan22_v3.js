// 周号到阶段的硬映射 (与 11408 的月份阶段完全对应)
//   week 0    : 准备期 (4/6 之前)
//   week 1-12 : 基础阶段 (4/6 Mon - 6/28 Sun, 12周)
//   week 13-21: 强化阶段 (6/29 Mon - 8/30 Sun, 9周)  ← 含7-8月 + 6/29-30
//   week 22-28: 真题阶段 (8/31 Mon - 10/18 Sun, 7周) ← 含9月+10月上中旬
//   week 29-36: 冲刺阶段 (10/19 Mon - 12/13 Sun, 8周)
//   week 37-38: 考前冲刺 (12/14 Mon - 12/21 考试日, ~1.5周)
function weekToPhase(week) {
  if (week <= 0) return 'prep';
  if (week <= 12) return 'base';
  if (week <= 21) return 'strong';
  if (week <= 28) return 'real';
  if (week <= 36) return 'sprint';
  return 'final';
}

