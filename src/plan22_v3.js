const WEEK_ORIGIN = new Date('2026-04-06T00:00:00');

function getWeekIndex(dateStr) {
  const d = new Date(dateStr + 'T00:00:00');
  const days = Math.floor((d - WEEK_ORIGIN) / 86400000);
  const week = days < 0 ? 0 : Math.floor(days / 7) + 1;
  let dayOfWeek = d.getDay();
  if (dayOfWeek === 0) dayOfWeek = 7; // 周日=7
  return { week, dayOfWeek };
}

function toCnDateLabel(dateStr) {
  const d = new Date(dateStr + 'T00:00:00');
  const weekdays = ['周日', '周一', '周二', '周三', '周四', '周五', '周六'];
  return `${d.getMonth() + 1}月${d.getDate()}日（${weekdays[d.getDay()]}）`;
}

function getDayOffset(dateStr, anchorDateStr) {
  const current = new Date(dateStr + 'T00:00:00');
  const anchor = new Date(anchorDateStr + 'T00:00:00');
  return Math.floor((current - anchor) / 86400000);
}

