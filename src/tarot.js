/**
 * 获取当月的塔罗牌提醒
 * @returns {Object|null} 包含 card / warning / study 的对象，如果当月没有提醒则返回 null
 */
export function getMonthlyTarot() {
  const now = new Date();
  const month = now.getMonth() + 1; // 1-12
  
  return TAROT_MONTHLY[month] || null;
}

/**
 * 格式化塔罗牌提醒为 Markdown 文本（同时展示通用提示和考研专属提示）
 * @returns {string} 格式化后的提醒文本
 */
export function formatTarotWarning() {
  const tarot = getMonthlyTarot();
  
  if (!tarot) {
    return ''; // 当月没有特殊提醒
  }
  
  let result = `### 🃏 本月塔罗提醒: ${tarot.card}\n\n`;
  result += `> ⚠️ **人生提示**: ${tarot.warning}\n\n`;
  if (tarot.study) {
    result += `> 📚 **考研专属**: ${tarot.study}\n\n`;
  }
  return result;
}
